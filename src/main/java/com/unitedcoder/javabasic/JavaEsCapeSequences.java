package com.unitedcoder.javabasic;

public class JavaEsCapeSequences {
    public static void main(String[] args) {
        // \t 	Insert a tab in the text at this point.
        System.out.println("a is a\t b");

        // \b Insert a backspace in the text at this point.
        System.out.println("a is a \b b" );

        // \n Insert a newline in the text at this point.
        System.out.println("\nThis is a new line");
        System.out.println("This is a new line\n");

        // \r Insert a carriage return in the text at this point.
        System.out.println("a is a \r b");

        // \\ Insert a backslash character in the text at this point.
        System.out.println("\\");

        // \' Insert a single quote character in the text at this point.
        System.out.println("He\'llo");

        // \" Insert a double quote character in the text at this point.
        System.out.println("\" ");
       // 

    }
}
